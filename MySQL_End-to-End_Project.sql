#IF & CASE Statement
SELECT * from Employees_Personal_Details

SELECT Emp_Name,Marital_Status,Age,
IF(Age<=25,'Young Employees','Old Employees')AS Result
FROM Employees_Personal_Details ORDER BY Age ASC

#CASE STATEMENT
SELECT Emp_Name,Marital_Status,Age,
CASE
WHEN Age <= 25 THEN 'Young Employees'
WHEN AGE BETWEEN 26 AND 59 THEN 'MID Age Employees'
WHEN AGE >59 THEN 'Aged Employees'
END AS AgeGroup
FROM Employees_Personal_Details ORDER BY AGE ASC

